// 모든 순간 일기를 가져오는 Mock API
export const getMockMoments = (params?: {
  limit?: number;
  before?: string;
  startDate?: string;
  endDate?: string;
}) => {
  let filteredMoments = [...mockMoments];

  if (params?.before && params.before !== 'undefined') {
    filteredMoments = filteredMoments.filter(
      m => m.momentTime < params.before!,
    );
  }

  const limit = params?.limit || 10;
  filteredMoments = filteredMoments.slice(0, limit);

  return {
    data: filteredMoments,
    resInfo: {
      dataCount: filteredMoments.length,
      nextCursor:
        filteredMoments[filteredMoments.length - 1]?.momentTime || null,
      hasMore: mockMoments.length > filteredMoments.length,
    },
  };
};

// 단일 순간 일기 상세 정보를 가져오는 Mock API
export const getMockMoment = (momentId: string) => {
  const id = parseInt(momentId);

  // 요청된 ID와 일치하는 상세 데이터 반환
  if (id === 101) {
    return mockMomentDetail;
  }

  // 상세 데이터가 없는 경우, 목록에서 해당 ID의 데이터를 찾아 반환
  const foundMoment = mockMoments.find(moment => moment.momentId === id);
  if (foundMoment) {
    return {
      momentId: foundMoment.momentId,
      momentTime: foundMoment.momentTime,
      images: foundMoment.images,
      content: foundMoment.content,
      tags: foundMoment.tags,
    };
  }

  // 데이터가 없는 경우
  throw new Error('Moment not found');
};

export default { getMockMoments, getMockMoment };
